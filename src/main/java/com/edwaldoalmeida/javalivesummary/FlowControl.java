package com.edwaldoalmeida.javalivesummary;

public class FlowControl {
    public static void main(String[] args) {
        // if
        boolean loop = true;
        if (loop) {
            // do something
        } else {
            // do something else
        } // there is no `finally` in Java

        // while
        int i = 0;
        while (i < 5) {
            // do something
            i += 1;
        }

        // do-while
        do {
            // do something
            i += 1;
        } while (i < 5);

        // for ("traditional")


        // for ("modern")





    }

}
